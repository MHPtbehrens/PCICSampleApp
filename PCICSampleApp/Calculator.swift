import Foundation

public class Calculator {
    
    func sum(a:Int, b:Int) -> Int {
        return a + b
    }
    
    func product(a:Int, b:Int) -> Int {
        return a * b
    }

    func difference(a:Int, b:Int) -> Int {
        return a - b
    }

    func division(a:Int, b:Int) -> Int {
        return a / b
    }

}